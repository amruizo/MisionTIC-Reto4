    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String newPassword = txtNewPassword.getText();
        String repeatPassword = txtRepeatPassword.getText();
        
        // Validar si las contraseñas ingresadas son iguales 
        if(newPassword.equals(repeatPassword)){
        JOptionPane.showMessageDialog(this, "Cambio Exitoso", "Datos Actualizados", JOptionPane.INFORMATION_MESSAGE);
        this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Contraseñas no coinciden", "Datos Actualizados",JOptionPane.ERROR_MESSAGE);
        }
        System.out.println("Contraseña nueva: ");
    }//GEN-LAST:event_btnSaveActionPerformed

